export function getExercisesByBodyPart(bodyPart: string): ExerciseInfo[] {
    return exerciseDatabase.filter(e => e.bodyPart === bodyPart)
}

export function searchExercises(query: string, bodyPartFilter?: string): ExerciseInfo[] {
    const lower = query.toLowerCase()
    return exerciseDatabase.filter(e => {
        const matchesQuery = e.name.toLowerCase().includes(lower)
        const matchesFilter = bodyPartFilter ? e.bodyPart === bodyPartFilter : true
        return matchesQuery && matchesFilter
    })
}
